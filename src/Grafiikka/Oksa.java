package Grafiikka;

/**
 * Koordinaatistopistettä, haarasukupolvea ja tulokulmaa säilövä olio,
 * jonka avulla Otukset piirretään.
 *
 * @author Lassi
 */
public class Oksa {

    /**
     * x-koordinaatin arvo.
     */
    private double x;
    /**
     * y-koordinaatin arvo.
     */
    private double y;
    /**
     * Monettako haaraa piirretään.
     */
    private int haara;
    /**
     * Tulokulma pisteeseen.
     */
    private double kulma;

    public Oksa(double x, double y, int haara, double kulma) {
        this.x = x;
        this.y = y;
        this.haara = haara;
        this.kulma = kulma;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public int getHaara() {
        return haara;
    }

    public double getKulma() {
        return kulma;
    }
}
